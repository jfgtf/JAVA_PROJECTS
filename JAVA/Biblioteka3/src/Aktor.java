public class Aktor {

    private String email;
    private String haslo;

    public Aktor(String email, String haslo) {
        this.email = email;
        this.haslo = haslo;
    }

    public Aktor() {
    }

    public String getEmail() {
        return email;
    }

    public String getHaslo() {
        return haslo;
    }
}
